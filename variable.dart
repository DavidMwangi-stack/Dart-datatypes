void main(){
  // Declaring variables with 'var'. It can be changed later
  var lesson = "dart flutter";
  // Declaring a variable with 'const'. Its value is set at compilex time
  const PI = 3.14;
  // Declaring a variable with 'final'. It cannot be changes once set.
  final date = 27;
  var name = "David Mburu";

  // Printing the values of the variables
print('lesson: $lesson' );
print('PI: $PI');
print('date: $date');
print('name: $name');


}